public class ÇarpımTablosu {
    public static void main(String[] args) {
        int sayi = 2;

        System.out.println(sayi + " sayısının çarpım tablosu:");
        for (int i = 1; i <= 10; i++) {
            System.out.println(sayi + " x " + i + " = " + (sayi * i));
        }
    }
}
